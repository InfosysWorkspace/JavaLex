package com.infosys.java8.dataTypes;

public class IdentifierDemo {
    public static void main(String[] args) {

        //Literals
        int custId = 1001;
        System.out.println("The Customer is: " +custId);

        //'L' is used to define a literal of type long
        long telephoneNo = 1234567890L;
        System.out.println("The Customer Phone No: " +telephoneNo);

        //'f' is used to define a literal of type float
        float billAmount = 123.45f;
        System.out.println("The Customer Billing Amount: " +billAmount);

        boolean isRegular = false;
        System.out.println("Is He a Regular Customer?: ");

    }
}
